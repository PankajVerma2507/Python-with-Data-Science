# Accesing tuple elements using slicing 
my_tuple = ('e', 'x','c','a','l','i','b','u')

# elements 2nd to 4th
# output: ('x', 'c', 'a')
print(my_tuple[1:4])

# elements begining to 2nd
# Output: ('e','x')
print(my_tuple[:-7])

# elements 8th to end
# output (u,r)
print(my_tuple[7:])
 # elements begining to end
print(my_tuple[:]) 